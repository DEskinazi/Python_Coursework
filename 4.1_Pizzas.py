# Store types of pizza in a list and loop through the list using a for loop. End the loop with a culminating sentence.
pizzas = ['cheese', 'pesto', 'hawaiian']
for pizza in pizzas:
	print(pizza)

# Create a for loop to write the same sentence for several items
	print(pizza.title() + " pizza is my favorite type of pizza.")
	print(pizza.title() + " pizza tastes really great.\n")

print("I really like pizza.")